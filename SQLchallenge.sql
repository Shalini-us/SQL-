--What is the total revenue generated by the sales of hatchback cars?
Select Sum(cars.cost_$)AS Total_Revenue_of_hatchback_cars
from cars cars join sales sales
on cars.car_id=sales.car_id
where cars.style='hatchback'

--What is the total revenue generated by the sales of SUV cars in the year 2022?
Select Sum(cars.cost_$)As Total_Revenue_of_SUVs_2022
FROM cars cars join sales sales
on cars.car_id=sales.car_id 
where cars.style='SUV' AND year(sales.purchase_date)=2022;

--What is the name and city of the salesperson who sold the most number of cars in the year 2023?

Select TOP(1)sp.salesman_id,sp.name,sp.city,count(sales.car_id)as max_cars_sold
from sales sales
join salespersons sp
on sales.salesman_id=sp.salesman_id
where year(purchase_date)=2023
group by sp.name,sp.city,sp.salesman_id
order by max_cars_sold DESC


-- What is the name and age of the salesperson who generated the highest revenue in the year 2022?
Select TOP(1)sp.name,sp.age,sp.salesman_id,MAX(cars.cost_$) as highest_salary
from salespersons sp
join sales sales
on sp.salesman_id=sales.salesman_id
join cars cars 
on cars.car_id=sales.car_id
where year(sales.purchase_date)= 2022
Group by sp.name,sp.age,sp.salesman_id
ORDER BY highest_salary DESC
