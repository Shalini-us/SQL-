--What are the details of all cars purchased in the year 2022?
Select cars.car_id,cars.make,cars.type,cars.cost_$,sales.sale_id,sales.purchase_date from cars cars JOIN sales sales
On cars.car_id=sales.car_id
where year(purchase_date)=2022
order by cars.car_id;

--What is the total number of cars sold by each salesperson?

Select sp.name,sp.salesman_id,count(sales.car_id)as total_number_of_cars_sold
from sales sales join salespersons sp on sales.salesman_id=sp.salesman_id
group by sp.name,sp.salesman_id;


--What is the total revenue generated by each salesperson?

Select sales.salesman_id, Sum(cars.cost_$)as total_revenue
From cars cars
JOIN sales sales
on cars.car_id=sales.car_id
group by sales.salesman_id

